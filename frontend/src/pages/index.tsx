import { GetServerSideProps, NextPage } from 'next'
import Head from 'next/head'
import styled from 'styled-components'
import { getEmployee } from '../services/employee/get-employee'
import { ApiContext } from '../types'
import Button from '../components/atoms/Button'
import React from 'react'
import ProjectCard from '../components/organisms/ProjectCard'
import { Grid } from '@mui/material'

const H1 = styled.h1`
  color: red;
`

const Title = styled.h1`
  font-size: ${({ theme }) => theme.fontSizes[4]};
  color: ${({ theme }) => theme.colors.primary};
  text-align: center;
`

type SSRProps = {
  employee: {
    ID: number
    Name: {
      FirstName: string
      LastName: string
    }
    Email: { Value: string }
    Password: { Value: string }
    Role: number
  }
}

const SSR: NextPage<SSRProps> = ({ employee }: SSRProps) => {
  if (!employee) return <div>loading...</div>

  return (
    <div>
      <Head>
        <title>Create Next App</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <main style={{ width: '100%' }}>
        <Title>
          Name is: {employee.Name.FirstName} {employee.Name.LastName}.
        </Title>
        <Grid container spacing={2} columns={16}>
          <Grid item xs={7}>
            <Button>Todo</Button>
          </Grid>
          <Grid
            item
            xs={8}
            sx={{
              bgcolor: 'gray',
              borderRadius: 3,
              p: 1,
              m: 5,
            }}
          >
            <ProjectCard />
            <ProjectCard />
            <ProjectCard />
          </Grid>
        </Grid>
        <div></div>
      </main>
    </div>
  )
}

export const getServerSideProps: GetServerSideProps<SSRProps> = async () => {
  const context: ApiContext = {
    apiRootUrl: process.env.API_BASE_URL || 'http://localhost:8090',
  }
  const employee = await getEmployee(context, { id: 1 })
  return {
    props: {
      employee,
    },
  }
}

export default SSR
